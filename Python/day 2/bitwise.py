a = 10  # 1010 in binary
b = 4   # 100 in binary

print("Bitwise AND (a & b):", a & b)  # 0
print("Bitwise OR (a | b):", a | b)  # 14 (1110 in binary)
print("Bitwise XOR (a ^ b):", a ^ b)  # 14 (1110 in binary)
print("Bitwise NOT (~a):", ~a)  # -11 (1111...1110...1111... in binary)
print("Bitwise Left Shift (a << 2):", a << 2)  # 40 (101000 in binary)
print("Bitwise Right Shift (a >> 1):", a >> 1)  # 5 (0101 in binary)